import java.util.*;

class Single extends Thread{
    public void run(){
        for(int i=0;i<5;i++){
            System.out.println(i);
        }
    }
}
public class SingleThreadInheritance {
    public static void main(String[] args) {
        Single t = new Single();
        t.start(); // start implicitly cass run method which is overide here.
    }
}
